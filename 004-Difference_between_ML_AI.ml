### Difference between Machine learning and Artificial Intelligence ###


  ==>Artificial Intelligence:
      “It is the study of how to train the computers so that computers
        can do things which at present human can do better.”



  ==>Machine Learning :
      “Machine Learning is said to learn from experience E w.r.t some
        class of task T and a performance measure P if learners performance
        at the task in the class as measured by P improves with experiences.”
