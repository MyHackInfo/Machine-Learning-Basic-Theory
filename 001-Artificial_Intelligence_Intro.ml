#### Artificial Intelligence | An Introduction ####

  ==>Intelligence, as we know, is the ability to acquire and apply
        the knowledge.Knowledge is the information acquired through experience.
        Experience is the knowledge gained through exposure(training).
        Summing the terms up, we get artificial intelligence as the
         “copy of something natural(i.e., human beings) ‘WHO’ are capable
         of acquiring and applying the information it has gained through exposure.”

  ==>Intelligence is composed of:
      1-Reasoning
      2-Learning
      3-Problem Solving
      4-Perception
      5-Linguistic Intelligence

  ==>Applications of AI include Natural Language Processing, Gaming, Speech Recognition,
      Vision Systems, Health care, Automotive etc.

  ==>AI has developed a large number of tools to solve the most difficult problems in computer science,
      like:
          1-Search and optimization
          2-Logic
          3-Probabilistic methods for uncertain reasoning
          4-Classifiers and statistical learning methods
          5-Neural networks
          6-Control theory
          7-Languages
